package it.csi.pgmeas.commons.repository;

import java.util.List;
import java.util.Optional;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import it.csi.pgmeas.commons.model.Fase;

@Repository
public interface FaseRepository extends JpaRepository<Fase, Integer> {

    // Metodo per cercare una Fase in base a faseCod
    Optional<Fase> findByFaseCod(@Param("faseCod") String faseCod);

    @Query("select a from Fase a "
			+ "where a.validitaInizio <= current_timestamp "
			+ "and (a.validitaFine is null or a.validitaFine >= current_timestamp) "
			+ "and (a.dataCancellazione is null or a.dataCancellazione >= current_timestamp) "
			+ "order by a.faseDesc")
	List<Fase> findAllValid();
}
